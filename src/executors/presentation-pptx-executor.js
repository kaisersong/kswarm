import { mkdirSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

export const PRESENTATION_PPTX_EXECUTOR_ID = 'local_pptx_executor_v1';

export function probePresentationPptxExecutor() {
  return {
    id: PRESENTATION_PPTX_EXECUTOR_ID,
    available: true,
    taskCapabilities: ['presentation_generation'],
    outputCapabilities: ['pptx'],
  };
}

export async function executePresentationPptxTask({
  projectId,
  task = {},
  workspacePath,
} = {}) {
  if (!workspacePath) return { ok: false, error: 'workspacePath required' };

  const artifactsDir = join(workspacePath, 'artifacts');
  mkdirSync(artifactsDir, { recursive: true });

  const slideCount = inferSlideCount(task);
  const filename = `${task.id || task.localTaskId || 'presentation'}-deck.pptx`;
  const path = join(artifactsDir, filename);
  const slides = buildSlides(task, slideCount);
  writeFileSync(path, createPptxBuffer(slides));

  return {
    ok: true,
    projectId,
    taskId: task.id || null,
    summary: `Fallback PPTX executor generated ${slideCount} slides for ${task.title || task.id || 'presentation task'}.`,
    slideCount,
    deliveryMode: 'fallback_executor',
    artifacts: [{
      filename,
      path,
      url: projectId ? `/projects/${projectId}/artifacts/${filename}` : undefined,
      previewable: false,
      mimeType: 'application/vnd.openxmlformats-officedocument.presentationml.presentation',
      deliveryMode: 'fallback_executor',
    }],
    delivery: {
      semantic: 'presentation',
      format: 'pptx',
      deliveryMode: 'fallback_executor',
      executorId: PRESENTATION_PPTX_EXECUTOR_ID,
    },
  };
}

function inferSlideCount(task = {}) {
  const text = `${task.title || ''}\n${task.brief || ''}\n${task.description || ''}\n${task.acceptanceCriteria || ''}`;
  const range = text.match(/(\d+)\s*[-至到]\s*(\d+)\s*页/);
  if (range) return clamp(Number(range[1]), 1, 40);
  const single = text.match(/(\d+)\s*页/);
  if (single) return clamp(Number(single[1]), 1, 40);
  return 16;
}

function buildSlides(task, slideCount) {
  const title = task.title || 'Presentation';
  const brief = task.brief || task.description || '';
  const slides = [{ title, body: brief || 'Generated by KSwarm fallback executor.' }];
  for (let i = 2; i <= slideCount; i++) {
    slides.push({
      title: `${title} ${i}`,
      body: brief || `Slide ${i}`,
    });
  }
  return slides;
}

function createPptxBuffer(slides) {
  const files = new Map();
  files.set('[Content_Types].xml', contentTypesXml(slides.length));
  files.set('_rels/.rels', rootRelsXml());
  files.set('ppt/presentation.xml', presentationXml(slides.length));
  files.set('ppt/_rels/presentation.xml.rels', presentationRelsXml(slides.length));
  for (let i = 0; i < slides.length; i++) {
    files.set(`ppt/slides/slide${i + 1}.xml`, slideXml(slides[i]));
  }
  return writeZip(files);
}

function contentTypesXml(slideCount) {
  const overrides = [];
  for (let i = 1; i <= slideCount; i++) {
    overrides.push(`<Override PartName="/ppt/slides/slide${i}.xml" ContentType="application/vnd.openxmlformats-officedocument.presentationml.slide+xml"/>`);
  }
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">
<Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>
<Default Extension="xml" ContentType="application/xml"/>
<Override PartName="/ppt/presentation.xml" ContentType="application/vnd.openxmlformats-officedocument.presentationml.presentation.main+xml"/>
${overrides.join('\n')}
</Types>`;
}

function rootRelsXml() {
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
<Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="ppt/presentation.xml"/>
</Relationships>`;
}

function presentationXml(slideCount) {
  const ids = [];
  for (let i = 1; i <= slideCount; i++) {
    ids.push(`<p:sldId id="${255 + i}" r:id="rId${i}"/>`);
  }
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<p:presentation xmlns:p="http://schemas.openxmlformats.org/presentationml/2006/main" xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships">
<p:sldIdLst>${ids.join('')}</p:sldIdLst>
<p:sldSz cx="12192000" cy="6858000" type="screen16x9"/>
</p:presentation>`;
}

function presentationRelsXml(slideCount) {
  const rels = [];
  for (let i = 1; i <= slideCount; i++) {
    rels.push(`<Relationship Id="rId${i}" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/slide" Target="slides/slide${i}.xml"/>`);
  }
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
${rels.join('\n')}
</Relationships>`;
}

function slideXml(slide) {
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<p:sld xmlns:p="http://schemas.openxmlformats.org/presentationml/2006/main" xmlns:a="http://schemas.openxmlformats.org/drawingml/2006/main">
<p:cSld><p:spTree><p:nvGrpSpPr><p:cNvPr id="1" name=""/><p:cNvGrpSpPr/><p:nvPr/></p:nvGrpSpPr><p:grpSpPr/>
<p:sp><p:nvSpPr><p:cNvPr id="2" name="Title"/><p:cNvSpPr/><p:nvPr/></p:nvSpPr><p:txBody><a:bodyPr/><a:lstStyle/><a:p><a:r><a:t>${xmlEscape(slide.title)}</a:t></a:r></a:p></p:txBody></p:sp>
<p:sp><p:nvSpPr><p:cNvPr id="3" name="Body"/><p:cNvSpPr/><p:nvPr/></p:nvSpPr><p:txBody><a:bodyPr/><a:lstStyle/><a:p><a:r><a:t>${xmlEscape(slide.body)}</a:t></a:r></a:p></p:txBody></p:sp>
</p:spTree></p:cSld></p:sld>`;
}

function writeZip(files) {
  const localParts = [];
  const centralParts = [];
  let offset = 0;

  for (const [name, content] of files.entries()) {
    const nameBuffer = Buffer.from(name);
    const data = Buffer.from(content);
    const crc = crc32(data);

    const local = Buffer.alloc(30);
    local.writeUInt32LE(0x04034b50, 0);
    local.writeUInt16LE(20, 4);
    local.writeUInt16LE(0, 6);
    local.writeUInt16LE(0, 8);
    local.writeUInt16LE(0, 10);
    local.writeUInt16LE(0, 12);
    local.writeUInt32LE(crc, 14);
    local.writeUInt32LE(data.length, 18);
    local.writeUInt32LE(data.length, 22);
    local.writeUInt16LE(nameBuffer.length, 26);
    local.writeUInt16LE(0, 28);
    localParts.push(local, nameBuffer, data);

    const central = Buffer.alloc(46);
    central.writeUInt32LE(0x02014b50, 0);
    central.writeUInt16LE(20, 4);
    central.writeUInt16LE(20, 6);
    central.writeUInt16LE(0, 8);
    central.writeUInt16LE(0, 10);
    central.writeUInt16LE(0, 12);
    central.writeUInt16LE(0, 14);
    central.writeUInt32LE(crc, 16);
    central.writeUInt32LE(data.length, 20);
    central.writeUInt32LE(data.length, 24);
    central.writeUInt16LE(nameBuffer.length, 28);
    central.writeUInt16LE(0, 30);
    central.writeUInt16LE(0, 32);
    central.writeUInt16LE(0, 34);
    central.writeUInt16LE(0, 36);
    central.writeUInt32LE(0, 38);
    central.writeUInt32LE(offset, 42);
    centralParts.push(central, nameBuffer);

    offset += local.length + nameBuffer.length + data.length;
  }

  const centralDirectory = Buffer.concat(centralParts);
  const end = Buffer.alloc(22);
  end.writeUInt32LE(0x06054b50, 0);
  end.writeUInt16LE(0, 4);
  end.writeUInt16LE(0, 6);
  end.writeUInt16LE(files.size, 8);
  end.writeUInt16LE(files.size, 10);
  end.writeUInt32LE(centralDirectory.length, 12);
  end.writeUInt32LE(offset, 16);
  end.writeUInt16LE(0, 20);
  return Buffer.concat([...localParts, centralDirectory, end]);
}

function crc32(buffer) {
  let crc = -1;
  for (const byte of buffer) {
    crc = (crc >>> 8) ^ CRC_TABLE[(crc ^ byte) & 0xff];
  }
  return (crc ^ -1) >>> 0;
}

const CRC_TABLE = Array.from({ length: 256 }, (_, n) => {
  let c = n;
  for (let k = 0; k < 8; k++) c = c & 1 ? 0xedb88320 ^ (c >>> 1) : c >>> 1;
  return c >>> 0;
});

function xmlEscape(value) {
  return String(value || '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&apos;');
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, Number.isFinite(value) ? value : min));
}
